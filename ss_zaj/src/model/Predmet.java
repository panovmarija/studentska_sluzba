package model;

import java.io.Serializable;

/**
 *
 * @author USER
 */
public class Predmet implements Serializable{
    private int sifra,esbp;
    private String naziv, kod;

    public Predmet() {
    }

    public Predmet(int sifra, int esbp, String naziv, String kod) {
        this.sifra = sifra;
        this.esbp = esbp;
        this.naziv = naziv;
        this.kod = kod;
    }

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public int getEsbp() {
        return esbp;
    }

    public void setEsbp(int esbp) {
        this.esbp = esbp;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public String getKod() {
        return kod;
    }

    public void setKod(String kod) {
        this.kod = kod;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Predmet other = (Predmet) obj;
        return this.sifra == other.sifra;
    }

    @Override
    public String toString() {
        return naziv;
    }
    
    
}
